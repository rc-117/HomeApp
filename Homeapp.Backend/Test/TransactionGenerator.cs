        private void GenerateTransactions()
        {
            var random = new Random();

            for (int i = 0; i < guids.Count; i++)
            {
                TransactionType type = random.Next(2) == 0 ?
                    TransactionType.Expense : TransactionType.Income;

                User user = TestRepo.Users[random.Next(TestRepo.Users.Count)];
                Account[] userAccounts = TestRepo.Accounts
                    .Where(account => account.UserId == user.Id)
                    .ToArray();
                var randomAccount = userAccounts[random.Next(userAccounts.Length)];

                DateTime dateTime = DateTime.Now
                    .AddDays(-random.Next(100))
                    .AddHours(-random.Next(24))
                    .AddMinutes(-random.Next(60));

                bool isCleared = random.Next(2) == 0 ? false : true;

                Transactions.Add(new Transaction
                {
                    Id = guids[i],
                    Name = names[random.Next(names.Count)],
                    Amount = random.Next(1000),
                    TransactionType = type,
                    User = user,
                    UserId = user.Id,
                    DateTime = dateTime,
                    Account = randomAccount,
                    AccountId = randomAccount.Id,
                    IsCleared = isCleared
                });

                guids.RemoveAt(i);
            }
        }
